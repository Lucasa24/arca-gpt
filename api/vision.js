// api/vision.js ‚Äî CommonJS + HTTP nativo
const OpenAI = require("openai");

// üîê Verifica√ß√£o de seguran√ßa no boot
console.log('üîë OPENAI_API_KEY configurada (vision):', !!process.env.OPENAI_API_KEY);
if (!process.env.OPENAI_API_KEY) {
  console.warn('‚ö†Ô∏è OPENAI_API_KEY n√£o encontrada! Configure no .env ou Vercel.');
}

module.exports = async function handler(req, res) {
  try {
    console.log('[VISION] Recebendo requisi√ß√£o:', req.method);
    
    if (req.method !== 'POST') {
      res.statusCode = 405;
      res.setHeader('Content-Type', 'application/json');
      return res.end(JSON.stringify({ error: 'M√©todo n√£o permitido' }));
    }

    const { prompt, imageB64, mime = "image/png" } = req.body || {};
    const api_key = process.env.OPENAI_API_KEY;
    
    if (!prompt || !imageB64) {
      res.statusCode = 400;
      res.setHeader('Content-Type', 'application/json');
      return res.end(JSON.stringify({ error: 'Prompt e imageB64 s√£o obrigat√≥rios' }));
    }
    
    if (!api_key) {
      res.statusCode = 500;
      res.setHeader('Content-Type', 'application/json');
      return res.end(JSON.stringify({ error: 'OPENAI_API_KEY ausente' }));
    }

    // aceita tanto base64 cru quanto dataURL j√° pronto
    const dataUrl = imageB64.startsWith("data:")
      ? imageB64
      : `data:${mime};base64,${imageB64}`;

    const client = new OpenAI({ apiKey: api_key });
    
    const completion = await client.chat.completions.create({
      model: "gpt-4o-mini",
      messages: [
        {
          role: "user",
          content: [
            { type: "text", text: prompt || "Analise a imagem e d√™ um diagn√≥stico execut√°vel." },
            { type: "image_url", image_url: { url: dataUrl } }
          ]
        }
      ],
      // opcional, ajuda a evitar respostas vazias
      max_tokens: 400
    });

    const text = completion?.choices?.[0]?.message?.content ?? "(sem conte√∫do)";
    res.writeHead(200, { "Content-Type": "application/json" });
    res.end(JSON.stringify({ text }));
  } catch (e) {
    console.error("Vision 500:", e?.response?.data || e);
    res.writeHead(500, { "Content-Type": "application/json" });
    res.end(JSON.stringify({ error: e?.message || "Erro desconhecido" }));
  }
};
